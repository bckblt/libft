#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*a;

	a = (char *)s;
	i = 0;
	while (*a)
	{
		if (*a == (char)c)
			return ((char *)a);
		a++;
	}
	if (*a == (char)c)
		return (a);
	return (NULL);
}
